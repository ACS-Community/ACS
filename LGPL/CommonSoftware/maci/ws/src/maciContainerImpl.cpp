bool
ContainerImpl::done()
{
  //ACS_TRACE("maci::ContainerImpl::done");

  doneCORBA();

  acsQoS::done();

  if(m_dllmgr)
      {
      m_dllmgr->unload("baci");  // baci has to be unloaded earlier because of DLLClose
      }

  if (m_database)
      {
      destroyDatabase(m_database);
      m_database = 0;
      }

  ACSError::done();

  return true;
}

