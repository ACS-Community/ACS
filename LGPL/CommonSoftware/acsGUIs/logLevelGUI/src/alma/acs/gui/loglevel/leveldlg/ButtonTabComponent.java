package alma.acs.gui.loglevel.leveldlg;

import java.awt.BasicStroke;
import java.awt.Color;
import java.awt.Component;
import java.awt.Dimension;
import java.awt.FlowLayout;
import java.awt.Graphics;
import java.awt.Graphics2D;
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;
import java.awt.event.MouseAdapter;
import java.awt.event.MouseEvent;
import java.awt.event.MouseListener;

import javax.swing.AbstractButton;
import javax.swing.BorderFactory;
import javax.swing.JButton;
import javax.swing.JLabel;
import javax.swing.JOptionPane;
import javax.swing.JPanel;

import javax.swing.plaf.basic.BasicButtonUI;

import alma.acs.gui.loglevel.LogLevelPanel;
import alma.acs.gui.loglevel.LogPaneNotFoundException;

/**
 * Component to be used as tabComponent;
 * Contains a JLabel to show the text and 
 * a JButton to close the tab it belongs to 
 */
@SuppressWarnings("serial")
public class ButtonTabComponent extends JPanel {

	private static final long serialVersionUID = -7111655172215857672L;

	// The tabbed pane
    private final LogLevelPanel pane;
    
    // The name of this tab
    private final LogLevelSelectorPanel selectorPane; 

	public ButtonTabComponent(final LogLevelPanel pane, LogLevelSelectorPanel selPane) {
        //unset default FlowLayout' gaps
        super(new FlowLayout(FlowLayout.LEFT, 0, 0));
        if (pane == null) {
            throw new NullPointerException("TabbedPane is null");
        }
        if (selPane== null) {
            throw new NullPointerException("LogLevelSelectorPanel is null");
        }
        this.pane = pane;
        this.selectorPane=selPane;
        setOpaque(false);
        
        //make JLabel read titles from JTabbedPane
        JLabel label = new JLabel() {
            public String getText() {
                int i = pane.indexOfTabComponent(ButtonTabComponent.this);
                if (i != -1) {
                    return pane.getTitleAt(i);
                }
                return null;
            }
        };
        
        add(label);
        //add more space between the label and the button
        label.setBorder(BorderFactory.createEmptyBorder(0, 0, 0, 5));
        //tab button
        JButton button = new TabButton();
        add(button);
        //add more space to the top of the component
        setBorder(BorderFactory.createEmptyBorder(2, 0, 0, 0));
    }

    private class TabButton extends JButton implements ActionListener {
        public TabButton() {
            int size = 17;
            setPreferredSize(new Dimension(size, size));
            setToolTipText("close this tab");
            //Make the button looks the same for all Laf's
            setUI(new BasicButtonUI());
            //Make it transparent
            setContentAreaFilled(false);
            //No need to be focusable
            setFocusable(false);
            setBorder(BorderFactory.createEtchedBorder());
            setBorderPainted(false);
            //Making nice rollover effect
            //we use the same listener for all buttons
            addMouseListener(buttonMouseListener);
            setRolloverEnabled(true);
            //Close the proper tab by clicking the button
            addActionListener(this);
        }

        /**
         * NOTE: there is only one source that can generate events: the button
         * 
         * @see java.awt.event.ActionListener
         */
        public void actionPerformed(ActionEvent e) {
        	if (selectorPane.userChangedLogLevels()) {
				if (JOptionPane.showConfirmDialog(
						null, 
						"Do you really want to discard changes?", 
						"Confirm", 
						JOptionPane.YES_NO_OPTION)==JOptionPane.NO_OPTION) {
					return;
				}
			}
        	try {
        		pane.removeLogSelectorTab(selectorPane.getName());
        	} catch (LogPaneNotFoundException ex) {
        		System.err.println("Log panel not found: "+selectorPane.getName());
        		int i = pane.indexOfTabComponent(ButtonTabComponent.this);
        		 if (i != -1) {
        			 pane.remove(i);
        			 System.err.println("Error recovered: tab "+selectorPane.getName()+" removed");
        		 } else {
        			 System.err.println("Impossible to remove tab named: "+selectorPane.getName());
        		 }
        	}
        }

        //we don't want to update UI for this button
        public void updateUI() {
        }

        //paint the cross
        protected void paintComponent(Graphics g) {
            super.paintComponent(g);
            Graphics2D g2 = (Graphics2D) g.create();
            //shift the image for pressed buttons
            if (getModel().isPressed()) {
                g2.translate(1, 1);
            }
            g2.setStroke(new BasicStroke(2));
            g2.setColor(Color.BLACK);
            if (getModel().isRollover()) {
                g2.setColor(Color.MAGENTA);
            }
            int delta = 6;
            g2.drawLine(delta, delta, getWidth() - delta - 1, getHeight() - delta - 1);
            g2.drawLine(getWidth() - delta - 1, delta, delta, getHeight() - delta - 1);
            g2.dispose();
        }
    }

    private final static MouseListener buttonMouseListener = new MouseAdapter() {
        public void mouseEntered(MouseEvent e) {
            Component component = e.getComponent();
            if (component instanceof AbstractButton) {
                AbstractButton button = (AbstractButton) component;
                button.setBorderPainted(true);
            }
        }

        public void mouseExited(MouseEvent e) {
            Component component = e.getComponent();
            if (component instanceof AbstractButton) {
                AbstractButton button = (AbstractButton) component;
                button.setBorderPainted(false);
            }
        }
    };
}